fn subt(a:i32, b:i32) -> i32{
    a - b
}


fn main() {

    let x = subt(14,77);
    println!("{x}");

    let sum = 2 + 2;
    let sub = 4 - 13;
    let div = 4 / 12;
    let mul = 5 * 10;
    let per = 5 % 10;

    println!("{sum}");
    println!("{sub}");
    println!("{div}");
    println!("{mul}");
    println!("{per}");

}
